namespace Meadow.Hardware;

/// <summary>
/// Enumerates the possible CAN bus bitrates.
/// </summary>
public enum CanBusBitrate
{
    /// <summary>
    /// Baud rate of 1 Mbps.
    /// </summary>
    Baud_1M,

    /// <summary>
    /// Baud rate of 800 kbps.
    /// </summary>
    Baud_800k,

    /// <summary>
    /// Baud rate of 500 kbps.
    /// </summary>
    Baud_500k,

    /// <summary>
    /// Baud rate of 250 kbps.
    /// </summary>
    Baud_250k
}
