namespace Meadow.Peripherals.Sensors.Location;

/// <summary>
/// Cardinal directions such as North, South, East, West, and Unknown.
/// </summary>
public enum CardinalDirection
{
    /// <summary>
    /// The direction North.
    /// </summary>
    North,

    /// <summary>
    /// The direction South.
    /// </summary>
    South,

    /// <summary>
    /// The direction East.
    /// </summary>
    East,

    /// <summary>
    /// The direction West.
    /// </summary>
    West,

    /// <summary>
    /// An unknown or invalid direction.
    /// </summary>
    Unknown
}
